package models.calendar;

public class CalendarRequest {
	
	private int year;  // 월
	private int month; // 달
	
	public int getYear() {
		return year;
	}
	
	public void setYear(int year) {
		this.year = year;
	}
	
	public int getMonth() {
		return month;
	}
	
	public void setMonth(int month) {
		this.month = month;
	}
}
